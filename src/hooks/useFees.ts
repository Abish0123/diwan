/**
 * When an Admission/SchoolFee invoice is paid IN FULL, advance the linked
 * Admissions Lead's pipeline stage the same way the old FinancePendingPayment
 * "Confirm Payment Received" flow used to — now driven by the real invoice
 * payment (Collections, or a parent/student self-paying via their own portal)
 * instead of a separate manual confirmation step. Plain exported function
 * (not part of the useFees() hook) so every payment-completion call site —
 * useFees.ts's own collectFee, and the parent/student portals' own
 * finalizeInvoicePayment (which write directly to the Invoice table and don't
 * go through this hook) — can call it with just the paid invoice in hand.
 */
export async function advanceLeadOnFeeInvoicePaid(invoice: Invoice, paymentMethod?: string): Promise<void> {
  if (!invoice.linkedLeadId) return;
  if (invoice.feeType !== 'Admission' && invoice.feeType !== 'SchoolFee') return;
  try {
    const now = new Date().toISOString();
    if (invoice.feeType === 'Admission') {
      await smartDb.update("Lead", invoice.linkedLeadId, {
        status: "Payment Done",
        admissionFeesPaid: true,
        paymentDate: now,
        invoiceNumber: invoice.invoiceNumber,
        updatedAt: now,
      });
    } else {
      await smartDb.update("Lead", invoice.linkedLeadId, {
        status: "Section Allocation",
        schoolFeesPaid: true,
        schoolFeePaymentDate: now,
        schoolFeesInvoice: invoice.invoiceNumber,
        updatedAt: now,
      });
    }

    // Email the parent a real payment receipt, and let finance/admin know
    // the lead advanced automatically — no manual "confirm payment" step.
    const lead: any = await smartDb.getOne("Lead", invoice.linkedLeadId).catch(() => null);
    const paymentType = invoice.feeType === 'Admission' ? 'Admission Fee' : 'Annual School Fee';
    if (lead?.email) {
      await sendInvoiceEmail({
        to: lead.email,
        toName: lead.parentName || invoice.studentName,
        studentName: invoice.studentName,
        invoiceNo: invoice.invoiceNumber,
        amount: invoice.amount,
        paymentType,
        paymentMethod: paymentMethod || "—",
        paidAt: now,
      }).catch(() => {});
    }
    const adminNotifId = `notif_${Date.now()}_admin_${invoice.id}`;
    await smartDb.create("Notification", {
      id: adminNotifId,
      uid: invoice.uid,
      audienceRole: "admin",
      type: "payment_confirmed",
      category: "finance",
      title: `${paymentType} Paid`,
      message: `${invoice.studentName} paid ${invoice.invoiceNumber} (${paymentType}) — lead automatically advanced to "${invoice.feeType === 'Admission' ? 'Payment Done' : 'Section Allocation'}".`,
      createdAt: now,
      time: now,
      read: false,
      invoiceNumber: invoice.invoiceNumber,
    }, adminNotifId).catch(() => {});
  } catch (error) {
    console.error("Failed to advance admissions lead after fee invoice payment:", error);
  }
}

export function useFees() {
  const { user } = useAuth();
  const queryClient = useQueryClient();
  const queryKey = ["fees-data"];

  // Called from ~8 places (Fees Management, parent/student fee pages, and
  // several dialogs) — each used to independently fire 3 parallel
  // smartDb.getAll calls (FeeStructure/FeeDiscount/Invoice) on mount.
  // react-query shares one fetch and one cache entry across all of them.
  const { data, isLoading: loading } = useQuery({
    queryKey,
    queryFn: async () => {
      // Unscoped — fee structures, discounts, and invoices are school-wide
      // financial records shared across every finance/admin account, not
      // private to whichever staff member happened to create them. Scoping
      // by creator uid made one admin's invoices (and, worse, the shared
      // invoice-number sequence) invisible to every other admin/accountant.
      const [structures, discounts, invs] = await Promise.all([
        smartDb.getAll("FeeStructure"),
        smartDb.getAll("FeeDiscount"),
        smartDb.getAll("Invoice")
      ]);
      return {
        structures: structures as FeeStructure[],
        discounts: discounts as FeeDiscount[],
        invoices: invs.map((i: any) => {
          const amount = Number(i.amount) || 0;
          const paidAmount = i.paidAmount !== undefined ? Number(i.paidAmount) : (i.status === 'Paid' ? amount : 0);
          const dueAmount = i.dueAmount !== undefined ? Number(i.dueAmount) : (i.status === 'Unpaid' || i.status === 'Overdue' ? amount : (i.status === 'Paid' ? 0 : amount - paidAmount));
          return { ...i, amount, paidAmount, dueAmount };
        }) as Invoice[],
      };
    },
    enabled: !!user,
  });

  const feeStructures = data?.structures ?? [];
  const feeDiscounts = data?.discounts ?? [];
  const invoices = data?.invoices ?? [];
  const fetchFeesData = () => queryClient.invalidateQueries({ queryKey });

  useEffect(() => {
    // Refetch whenever any invoice/payment mutation happens anywhere in the
    // app (Collections, Due Tracking, parent portal payment, etc.) so every
    // consumer of useFees() stays in sync without a manual page reload.
    window.addEventListener("fees-updated", fetchFeesData);
    return () => window.removeEventListener("fees-updated", fetchFeesData);
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, []);

  const createFeeStructure = async (data: Omit<FeeStructure, 'id' | 'uid' | 'createdAt'>) => {
    if (!user) return;
    try {
      await smartDb.create("FeeStructure", {
        ...data,
        uid: user.uid,
        createdAt: new Date().toISOString()
      });
      fetchFeesData();
      toast.success("Fee structure created successfully");
    } catch (error) {
      handleFirestoreError(error, OperationType.CREATE, "FeeStructure");
    }
  };

  const updateFeeStructure = async (id: string, data: Partial<Omit<FeeStructure, 'id' | 'uid' | 'createdAt'>>) => {
    if (!user) return;
    try {
      await smartDb.update("FeeStructure", id, { ...data, updatedAt: new Date().toISOString() });
      fetchFeesData();
      toast.success("Fee structure updated successfully");
    } catch (error) {
      handleFirestoreError(error, OperationType.UPDATE, "FeeStructure");
    }
  };

  const deleteFeeStructure = async (id: string) => {
    if (!user) return;
    try {
      await smartDb.delete("FeeStructure", id);
      fetchFeesData();
      toast.success("Fee structure deleted");
    } catch (error) {
      handleFirestoreError(error, OperationType.DELETE, "FeeStructure");
    }
  };

  /**
   * Bulk-create fee structures from an imported file (one row per grade).
   * Returns { created, skipped } so the caller can show an accurate summary —
   * rows are skipped (not silently fabricated) when they don't resolve to a
   * real class or have no usable amount, rather than inventing a classId.
   */
  const bulkImportFeeStructures = async (
    rows: Omit<FeeStructure, 'id' | 'uid' | 'createdAt'>[]
  ): Promise<{ created: number; skipped: number }> => {
    if (!user) return { created: 0, skipped: rows.length };
    let created = 0;
    let skipped = 0;
    for (const row of rows) {
      if (!row.classId || !row.components?.length) { skipped++; continue; }
      try {
        await smartDb.create("FeeStructure", {
          ...row,
          uid: user.uid,
          createdAt: new Date().toISOString(),
        });
        created++;
      } catch (error) {
        console.error("Error importing fee structure row:", row, error);
        skipped++;
      }
    }
    fetchFeesData();
    return { created, skipped };
  };

  const generateInvoicesForClass = async (classId: string, feeStructureId: string) => {
    if (!user) return;
    try {
      const structure = feeStructures.find(s => s.id === feeStructureId);
      if (!structure) throw new Error("Fee structure not found");

      // Student.classId is populated at enrollment with the grade LABEL (e.g. "Grade 5"),
      // not the Class document's auto-generated id — so match on the fee structure's
      // human-readable className (and each student's grade field) instead of the raw
      // classId, which would otherwise never match any real student record.
      //
      // Unscoped: Student.uid is stamped with whichever staff account created
      // the row (see localDb.ts), not a real ownership boundary — scoping by
      // the CURRENT user's uid here would silently skip every student some
      // other admin enrolled, same convention StudentContext.tsx already uses.
      const students = await smartDb.getAll("Student") as Student[];
      const classStudents = students.filter((s: Student) =>
        s.classId === classId ||
        s.classId === structure.className ||
        (s as any).grade === structure.className
      );

      if (classStudents.length === 0) {
        toast.error(`No students found in ${structure.className}. Check that students are enrolled with a matching grade.`);
        return;
      }

      // Prefer a real per-term breakdown (Term 1/2/3) when the structure has
      // term-labeled components; otherwise fall back to one annual invoice.
      const termRows = buildTermInvoiceRows(structure);
      const rows = termRows ?? [{
        term: undefined,
        termNumber: undefined,
        amount: structure.totalAmount,
        dueDate: new Date(Date.now() + 30 * 24 * 60 * 60 * 1000).toISOString(),
      }];

      // Sequential invoice numbering (INV-YYYY-NNNNNN) instead of a random
      // suffix — most jurisdictions require invoices to be numbered in an
      // unbroken sequence for tax filing. Computed from the current max
      // sequence in this year, then incremented synchronously for every
      // invoice in this batch so none collide. Unscoped for the same reason
      // as nextInvoiceNumber() above — the sequence is school-wide.
      const year = new Date().getFullYear();
      const existingInvoices = await smartDb.getAll("Invoice");
      const seqPattern = new RegExp(`^INV-${year}-(\\d+)$`);
      let nextSeq = existingInvoices.reduce((max: number, inv: any) => {
        const m = seqPattern.exec(inv.invoiceNumber || "");
        return m ? Math.max(max, parseInt(m[1], 10)) : max;
      }, 0) + 1;
      const nextInvoiceNumber = () => `INV-${year}-${String(nextSeq++).padStart(6, "0")}`;

      const batchPromises = classStudents.flatMap((student: Student) =>
        rows.map(row => {
          const invoiceNumber = nextInvoiceNumber();
          return smartDb.create("Invoice", {
            invoiceNumber,
            studentId: student.id,
            studentName: student.name,
            classId: student.classId,
            className: structure.className,
            category: structure.name,
            term: row.term,
            termNumber: row.termNumber,
            amount: row.amount,
            paidAmount: 0,
            dueAmount: row.amount,
            dueDate: row.dueDate,
            status: 'Unpaid',
            penalty: 0,
            uid: user.uid,
            createdAt: new Date().toISOString()
          });
        })
      );

      await Promise.all(batchPromises);
      fetchFeesData();
      window.dispatchEvent(new Event("fees-updated"));

      // Notify each affected student and their parent(s) in-app. Targeted the
      // same way parent fee reminders are (see feeReminderEngine.ts):
      // recipientUid = the student's own id for the student portal, and each
      // parent's email for the parent portal (there's no separately-stored
      // parent account uid — email is how parent sessions are identified).
      const termLabel = rows.length > 1 ? `${rows.length} term invoices` : "an invoice";
      await Promise.allSettled(
        classStudents.flatMap((student: any) => {
          const notifId = `notif_${Date.now()}_${student.id}`;
          const message = `${termLabel} generated for ${student.name} — ${structure.name} (${structure.className}). First due ${new Date(rows[0].dueDate).toLocaleDateString()}.`;
          const recipients = [
            student.id,
            ...[student.fatherEmail, student.motherEmail, student.guardianEmail].filter(Boolean),
          ];
          return recipients.map((recipientUid, i) =>
            smartDb.create(
              "Notification",
              {
                id: `${notifId}_${i}`,
                recipientUid,
                type: "invoice_generated",
                title: "New Fee Invoice",
                message,
                createdAt: new Date().toISOString(),
                time: new Date().toISOString(),
                read: false,
              },
              `${notifId}_${i}`,
            )
          );
        })
      );

      toast.success(
        `Invoices generated for ${classStudents.length} students` +
        (rows.length > 1 ? ` (${rows.length} term invoices each)` : '') +
        " — students and parents notified"
      );
    } catch (error) {
      console.error("Error generating invoices:", error);
      toast.error("Failed to generate invoices");
    }
  };

  /**
   * Generate ONE real invoice for a single admissions lead — the manual
   * "Generate Invoice" action a finance user takes for Admission/School Fee
   * structures, replacing the old auto-created FinancePendingPayment record.
   * Uses the same sequential INV-YYYY-NNNNNN numbering as bulk class invoices
   * so admission/school fee invoices share one unbroken sequence with every
   * other invoice, not a separate random one.
   */
  const generateSingleInvoice = async (input: {
    studentId: string;
    studentName: string;
    classId?: string;
    className: string;
    feeStructureId: string;
    feeType: 'Admission' | 'SchoolFee';
    linkedLeadId?: string;
    dueInDays?: number;
    recipientEmail?: string; // parent/lead email — sends the "please pay" invoice notice when known
  }): Promise<Invoice | undefined> => {
    if (!user) return;
    try {
      const invoice = await createLeadFeeInvoice({
        uid: user.uid,
        leadId: input.linkedLeadId || input.studentId,
        studentName: input.studentName,
        className: input.className,
        feeType: input.feeType,
        feeStructureId: input.feeStructureId,
        dueInDays: input.dueInDays,
      });
      if (!invoice) { toast.error("Fee structure not found"); return; }

      fetchFeesData();

      await smartDb.create("Notification", {
        recipientUid: input.studentId,
        type: "invoice_generated",
        title: input.feeType === 'Admission' ? "Admission Fee Invoice" : "School Fee Invoice",
        message: `${invoice.category} invoice ${invoice.invoiceNumber} generated for ${input.studentName} — due ${new Date(invoice.dueDate).toLocaleDateString()}.`,
        createdAt: new Date().toISOString(),
        time: new Date().toISOString(),
        read: false,
      }).catch(() => {});

      if (input.recipientEmail) {
        await sendInvoiceGeneratedEmail({
          to: input.recipientEmail,
          toName: input.studentName,
          studentName: input.studentName,
          invoiceNo: invoice.invoiceNumber,
          amount: invoice.amount,
          paymentType: input.feeType === 'Admission' ? 'Admission Fee' : 'Annual School Fee',
          dueDate: invoice.dueDate,
        }).catch(() => {});
      }

      toast.success(`${invoice.invoiceNumber} generated for ${input.studentName}`);
      return invoice;
    } catch (error) {
      console.error("Error generating single invoice:", error);
      toast.error("Failed to generate invoice");
    }
  };

  const updateInvoiceStatus = async (invoiceId: string, status: Invoice['status']) => {
    try {
      await smartDb.update("Invoice", invoiceId, { status });
      fetchFeesData();
      window.dispatchEvent(new Event("fees-updated"));
      toast.success("Invoice status updated");
    } catch (error) {
      handleFirestoreError(error, OperationType.UPDATE, "Invoice");
    }
  };

  /**
   * Recompute + persist an invoice's late fee (penalty) and derived due amount.
   * Silent (no toast) since this runs automatically on every Due Tracking load,
   * not from a direct user action — surfacing a toast per invoice would be noisy.
   */
  const updateInvoicePenalty = async (invoiceId: string, penalty: number) => {
    try {
      const invoice = invoices.find(inv => inv.id === invoiceId);
      if (!invoice) return;
      const dueAmount = Math.max(0, invoice.amount + penalty - invoice.paidAmount);
      await smartDb.update("Invoice", invoiceId, { penalty, dueAmount });
      fetchFeesData();
    } catch (error) {
      console.error("Error updating invoice penalty:", error);
    }
  };

  const collectFee = async (invoiceId: string, amount: number, paymentMethod: string, date: string): Promise<Invoice | undefined> => {
    if (!user) return;

    try {
      const invoice = invoices.find(inv => inv.id === invoiceId);
      if (!invoice) throw new Error("Invoice not found");

      const newPaidAmount = (invoice.paidAmount || 0) + amount;
      const newDueAmount = Math.max(0, (invoice.amount + (invoice.penalty || 0)) - newPaidAmount);
      const newStatus: Invoice['status'] = newDueAmount === 0 ? 'Paid' : 'Partial';

      // 1. Update Invoice
      await smartDb.update("Invoice", invoiceId, {
        paidAmount: newPaidAmount,
        dueAmount: newDueAmount,
        status: newStatus,
        updatedAt: new Date().toISOString()
      });

      // 2. Record Revenue
      await smartDb.create("StudentRevenue", {
        student: invoice.studentName,
        studentId: invoice.studentId,
        invoiceId: invoiceId,
        amount: amount,
        category: "Tuition Fee",
        date: date,
        paymentMethod: paymentMethod,
        status: "Paid",
        uid: user.uid,
        createdAt: new Date().toISOString(),
        updatedAt: new Date().toISOString()
      });

      const paidInvoice = { ...invoice, paidAmount: newPaidAmount, dueAmount: newDueAmount, status: newStatus };
      // If this was an Admission/School Fee invoice paid in full, advance the
      // linked admissions lead the same way the old manual "Confirm Payment
      // Received" step used to — automatically, from the real payment.
      if (newStatus === 'Paid') await advanceLeadOnFeeInvoicePaid(paidInvoice, paymentMethod);

      fetchFeesData();
      // Notify StudentContext so the student's fee status / Pending Fees filter refreshes.
      window.dispatchEvent(new Event("fees-updated"));
      toast.success(`Payment of ${amount} recorded for ${invoice.studentName}`);
      return paidInvoice;
    } catch (error) {
      handleFirestoreError(error, OperationType.WRITE, "Invoice/StudentRevenue");
      throw error;
    }
  };

  const createFeeDiscount = async (data: Omit<FeeDiscount, 'id' | 'uid' | 'createdAt'>) => {
    if (!user) return;
    try {
      await smartDb.create("FeeDiscount", {
        ...data,
        uid: user.uid,
        createdAt: new Date().toISOString()
      });
      fetchFeesData();
      toast.success("Fee discount created successfully");
    } catch (error) {
      handleFirestoreError(error, OperationType.CREATE, "FeeDiscount");
    }
  };

  return {
    feeStructures,
    feeDiscounts,
    invoices,
    loading,
    createFeeStructure,
    updateFeeStructure,
    deleteFeeStructure,
    bulkImportFeeStructures,
    createFeeDiscount,
    generateInvoicesForClass,
    generateSingleInvoice,
    updateInvoiceStatus,
    updateInvoicePenalty,
    collectFee
  };
}
